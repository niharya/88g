import sharp from 'sharp'
import { rgbaToThumbHash } from 'thumbhash'
import { readdir, writeFile, stat } from 'node:fs/promises'
import path from 'node:path'

const ROOT = path.resolve('public')
const OUT_FILE = path.resolve('app/components/Img/manifest.generated.ts')
const RASTER = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif'])
// Walk the whole public/ tree so any raster under public/images/, public/marks/, etc.
// is picked up automatically. Non-photo rasters (og, apple-touch-icon) get entries
// but never get consumed — harmless overhead.

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true })
  const out = []
  for (const e of entries) {
    const full = path.join(dir, e.name)
    if (e.isDirectory()) out.push(...(await walk(full)))
    else if (RASTER.has(path.extname(e.name).toLowerCase())) out.push(full)
  }
  return out
}

async function processOne(abs) {
  const src = '/' + path.relative(ROOT, abs).split(path.sep).join('/')
  const img = sharp(abs)
  const { dominant } = await img.stats()
  const dominantColor = `rgb(${dominant.r}, ${dominant.g}, ${dominant.b})`

  const { data, info } = await sharp(abs)
    .resize(100, 100, { fit: 'inside' })
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true })

  const hashBytes = rgbaToThumbHash(info.width, info.height, data)
  const thumbHash = Buffer.from(hashBytes).toString('base64')

  const meta = await img.metadata()
  return [src, { dominantColor, thumbHash, width: meta.width, height: meta.height }]
}

const files = await walk(ROOT)
const entries = await Promise.all(files.map(processOne))
const manifest = Object.fromEntries(entries)

const out = `// Generated by scripts/generate-image-manifest.mjs — do not edit by hand.
// Run \`npm run lqip\` to regenerate after adding or replacing images.

export type ImageManifestEntry = {
  dominantColor: string
  thumbHash: string
  width: number
  height: number
}

export const imageManifest: Record<string, ImageManifestEntry> = ${JSON.stringify(manifest, null, 2)}
`

await writeFile(OUT_FILE, out)
console.log(`Wrote ${entries.length} entries to ${path.relative(process.cwd(), OUT_FILE)}.`)
