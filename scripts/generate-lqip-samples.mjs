import sharp from 'sharp'
import { rgbaToThumbHash } from 'thumbhash'
import { writeFile } from 'node:fs/promises'
import path from 'node:path'

const SAMPLES = [
  { label: 'UI mockup', src: '/images/rr/rr-card-v5.png' },
  { label: 'Sketch',    src: '/images/rr/rr-sketch-1.jpg' },
  { label: 'Photo',     src: '/images/biconomy/sa/design-team-bico.webp' },
  { label: 'Poster',    src: '/images/biconomy/multiverse_poster.webp' },
]

const ROOT = path.resolve('public')

async function processOne({ label, src }) {
  const abs = path.join(ROOT, src)
  const img = sharp(abs)

  const { dominant } = await img.stats()
  const dominantColor = `rgb(${dominant.r}, ${dominant.g}, ${dominant.b})`

  const { data, info } = await sharp(abs)
    .resize(100, 100, { fit: 'inside' })
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true })

  const hashBytes = rgbaToThumbHash(info.width, info.height, data)
  const thumbHash = Buffer.from(hashBytes).toString('base64')

  const meta = await sharp(abs).metadata()

  return { label, src, dominantColor, thumbHash, width: meta.width, height: meta.height }
}

const results = await Promise.all(SAMPLES.map(processOne))

const out = `// Generated by scripts/generate-lqip-samples.mjs — do not edit by hand.
export const lqipSamples = ${JSON.stringify(results, null, 2)} as const
`

await writeFile(path.resolve('app/_dev-tools/lqip-lab/samples.generated.ts'), out)
console.log(`Wrote ${results.length} samples.`)
